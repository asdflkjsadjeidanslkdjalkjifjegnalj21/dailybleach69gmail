                        <p class="text-light text-box-margin">
Clubs en scholen krijgen na aanmelding een eigen plek binnen de Online Jeugdschaakclub Nederland onder groepen. Een volwassene (de contactpersoon) krijgt het beheer over de groep en kan de eigen kinderen toegang geven tot deze groep.
Hierbij een overzicht van de te doorlopen stappen:
</p>
<ol class="text-light">
    <li>
)Aanmelden bij de KNSB om voordelig goldaccounts voor Chesskid in te kopen;
Hier wordt de naam en adres van de school gevraagd. Kort de schoolnaam eventueel af naar onder de 25 karakters. De naam van de school wordt namelijk ook gebruikt in de gebruikersnamen van de kinderen. Het opgegeven emailadres van de contactpersoon (moet een volwassene zijn) wordt tevens gebruikt voor de toegang tot Chesskid. 
Van de kinderen wordt de voornaam, tussenvoegsel, achternaam en geboorte datum gevraagd. De voornaam komt ook terug in de gebruikersnaam. Een volledige naam en geboorte datum is nodig voor de identificatie en registratie in de administratie als KNSB scholierenlid.
De gebruikersnamen van de kinderen worden door de schaakbond gegenereerd op basis van de school of clubnaam en de voornaam.
</li>
<li>
) De KNSB zal de contactpersoon vervolgens uitnodigen voor Chesskid als beheerder van de groep. Valideer het account door op de link in de toegestuurde email te klikken. U kunt nu voor uzelf een account aanmaken met een zelfgekozen avatar (plaatje). Als u al een account bij Chesskid had, dan wordt deze automatisch  aan de nieuwe groep gekoppeld. 
De naam van de groep bestaat uit het schoolnummer of clubnummer gevolgd door de school- of clubnaam. Kinderen kunnen van meerdere groepen lid zijn (bv een school en een club). 
</li>
<li>
) De KNSB zal zorgen dat het aantal ingekochte accounts voor de kinderen worden geactiveerd. Let wel het jaarlidmaatschap van alle aangevraagde accounts  gaat direct lopen vanaf het moment van aanvragen, tenzij een andere activatiedatum is gecommuniceerd. Er kunnen dus geen accounts worden opgespaard voor in de toekomst. Dit heeft te maken met de bulk inkoopprijs.
</li>
<li>
) Voor je groep biedt Chesskid een paar hulpmiddelen om de logins aan de eigen kinderen te verstrekken. Login in Chesskid en ga naar je groep.
Aan de rechterzijde van het menu staat het volgende:
· Print login kaartjes voor de leerlingen met hun usernaam & wachtwoord.
· Plaats een aankondiging  inclusief een link naar wat je je leerlingen wilt laten doen zodra ze zijn ingelogd.
</li>
<li>
) Alle via de KNSB aangemelde scholen, clubs en kinderen worden automaticsh lid van de club Online Jeugdschaakclub Nederland. Daarnaast zullen de komende tijd nog extra deelclubs worden ingericht. Clubs binnen Chesskid betekenen toernooien of competities. Kinderen kunnen lid worden van deze clubs om uitnodigingen te ontvangen. Clubs kunnen bepaalde eisen stellen biijvoorbeeld een bepaalde rating grens (onder of boven) of een bepaalde groep op een school. 
</li>
